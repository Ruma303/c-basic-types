#include <stdio.h>

int main() {

    // Variabili floating point
    float e = 2.71828f;
    printf("Il valore del numero di Nepero è: %f\n", e);

    double pi = 3.14;
    printf("Il valore di pi greco è: %lf\n", pi);

    long double pi_long = 3.14159265359;
    printf("Il valore di pi greco è: %Lf\n", pi_long);

    return 0;
}